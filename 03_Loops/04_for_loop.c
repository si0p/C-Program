# include<stdio.h>
int main(){
    // normal case of for loop:
    //Printing first 10 natural numbers
    for(int a=1;a<=10;a++){
        printf("The first 10 natural no. are %d\n",a);
    }
    //Different case fo for loop:
    //Printing first 10 natural numbers in reverse
    for(int b=10;b;b--)
    printf("First 10 natural no. in reverse  %d\n",b);
return 0;
}